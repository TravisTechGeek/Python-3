# Boolean Variables
# First Variable
my_baby_bool = "true"
# Checking the type of function
type(my_baby_bool) # You will need. to print the the function to have it displayed
print(type(my_baby_bool)) # Returns <class 'str'> which verifies that this is not a boolean variable, boolean values are never in quotes and are always capitalized

# Second Variable
my_baby_bool_two = True
# Checking the type of function
type(my_baby_bool_two) # You will need. to print the the function to have it displayed
print(type(my_baby_bool_two)) # Returns <class 'bool'> proving this is a boolean variable